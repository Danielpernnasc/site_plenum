                        <h2>Resumo</h2>
                    </div>
                    <div class="text-prof col-md-8">
                        <p>O tratamento restaurador com implantes osseointegráveis possibilita a reabilitação do sistema estomatognático, restabelendo função, estética e fonética
                        sem danificar as estruturas nobres intrabucais. Para esses objetivos serem alcançados deve-se ter um planejamento minucioso antes da instalação dos
                        implantes dentários. O objetivo deste trabalho é explanar quais são as tecnologias que auxiliam no planejamento cirúrgico em Implantodontia. O principal
                        avanço foi o uso da tomografia computadoriza que possibilitou a construção de imagens tridimensionais (3D), facilitando o diagnóstico e o plano de
                        tratamento, abrindo novos caminhos para o planejamento cirúrgico. Essas imagens são processadas no formato DICOM e enviadas para um computador
                        para a realização de um planejamento virtual por meio de softwares específicos para determinar o posicionamento 3D do implante. Através da tecnologia
                        CAD/CAM e da prototipagem rápida foi possível a transferência do planejamento virtual para o momento cirúrgico através dos guias cirúrgicos
                        estereoligrafados. Conclui-se que com o avanço das imagens radiográficas foi possível a realização de um planejamento totalmente virtual, sendo possível a
                        transferência desses para o ato cirúrgico, ocasionando um resultado mais previsível, com maior segurança e conforto para o paciente.</p>
                        <p><b>Palavras-chave: Implantes dentários; Tomografia Computadorizada de Feixe Cônico, Cirurgia Assistida por computador</b></p>
                    </div>
                </div>
                <div id="texto_profissional" class="row">
                    <div class="titulo-prof col-md-4">
                        <h2>Introdução</h2>
                    </div>
                    <div class="text-prof col-md-8">
                        <p>
                        O grande desafio da Odontologia ao longo do tempo é restabelecer função, fonética e estética devolvendo saúde e bem estar ao paciente. Após as publicações
                        de Per-Ingvar Branemark sobre a osseointegração, a Implantodontia tornou-se uma alternativa terapêutica importante na reabilitação dos pacientes
                        (CARVALHO et al., 2006). Os objetivos do tratamento restaurador com implantes osseointegráveis estão relacionados à devolução da função e da estética
                        sem danificar as estruturas nobres intrabucais. Esses, só serão alcançados através de um planejamento minucioso antes da instalação dos implantes que
                        conta com avaliação médica, odontológica, radiográfica, modelos de estudo e modelos fotográficos (CARVALHO et al., 2006). Dentre tantas ferramentas, o
                        diagnóstico por imagens é considerado uma das etapas mais importantes para um tratamento previsível e com altos índices de sucesso (COMANDULLI et
                        al., 2005).
                        </p><br>
                        <p>O principal papel de qualquer método de aquisição de imagens para o planejamento de implantes é proporcionar informações adequadas sobre a morfologia
                        óssea e a localização das estruturas nobres que devem ser preservadas, além do fornecimento de informações sobre a densidade óssea (CHAU; FUNG, 2009).
                        A radiografia panorâmica e a radiografia digital são bons métodos para a avaliação dessas estruturas, no entanto esses exames radiográficos geram uma
                        imagem bidimensional (2D), dificultando a interpretação da imagem devido à sobreposição das estruturas ósseas (LIANG et al., 2010).</p><br>
                        <p>Com o avanço das técnicas imaginológicas principalmente através da tomografia computadorizada (TC) houve um grande salto no planejamento em
                        Implantodontia, pois os
                        tomógrafos geram imagens tridimensionais (3D), permitindo visualização e análise mais detalhada da região a ser implantada. (MENEZES; DARMENTO;
                        LAMBERT, 2008). Dados da TC são processados em um formato digital imaging and communications in medicine (DICOM), e transferidas para softwares
                        específicos para o planejamento dos implantes (THOMÉ, 2007). O planejamento pré-operatório, com os softwares, permite ao cirurgião verificar a forma
                        de estruturas anatômicas e evitar complicações cirúrgicas (CREMONINI et al., 2011).</p><br>
                        <p>A obtenção de protótipos é possível graças à integração da tecnologia de aquisição e manipulação das imagens com o sistema computer-aided desing (CAD) e
                        sistemas de prototipagem rápida (MEURER et al., 2008). A prototipagem rápida é uma técnica que permite a realização da cirurgia guiada, através dos guias
                        cirúrgicos que objetivam guiar o implante na posição pré-determinada pelo software (CHOI; ROMBERG, DRISCOLL, 2004). A técnica da cirurgia guiada
                        apresenta muitas vantagens se comparada às técnicas convencionais como maior conforto para o paciente, diminuição de edemas pós-cirúrgicos e redução
                        do tempo da cirurgia (THOMÉ et al., 2009). Assim, o objetivo deste estudo é explanar quais são as tecnologias que auxiliam no planejamento cirúrgico em
                        Implantodontia.</p>
