            <h3>CÓDIGO DE CONDUTA PLENUM</h3><br>
            <p>Este código é voltado para todos, o que inclui os colaboradores desde a Alta Administração até o chão de
            fábrica, bem como os parceiros de negócios, o que inclui fornecedores, distribuidores e prestadores de
            serviços. Todos prestam contas pelo cumprimento deste Código.</p><br>
            <p>O colaborador ou parceiro de negócio da Plenum deve seguir este Código, assim como deve se familiarizar
            com ele, bem como com os outros regulamentos internos, políticas, procedimentos operacionais padrão e
            diretrizes, conforme o caso.</p><br>
            <p>Em especial, os colaboradores da Plenum também devem estar familiarizados com as leis que se aplicam a seu
            campo de trabalho e da sua supervisão. Se houver qualquer conflito entre as autoridades locais,
            leis ou regulamentos e este Código, deve-se sempre cumprir a lei ou regulamentos aplicáveis.</p>
            <p>Em caso de dúvidas sobre as leis ou regulamentos internos da Plenum, entre em contato com o nosso
            Departamento Jurídico.</p><br>
           <h4>Clientes</h4><br>

            <p>As relações com os clientes devem ser baseadas na conduta ética, transparente e eficaz, sendo que o foco da
                atuação da Plenum está diretamente ligada à qualidade, confiabilidade de produtos e
                serviços, respeitando-se as necessidades, a integridade, as expectativas e privacidade dos clientes da
                empresa, orientados pela melhoria continua de processos e desempenho, através da eficiência, inovação e de
                excelência no atendimento dos clientes.</p><br>
            <p>Assim, deve-se manter sigilo absoluto sobre as informações cadastrais dos clientes, mantendo-as
                confidenciais sendo terminantemente proibido o seu repasse para ambiente digitalizado ou impresso visando
                a divulgação destas informações, cabendo sanções e punições deste código e da legislação vigente. O
                relacionamento da empresa com seus clientes é justo e imparcial. É proibido oferecer propina, descontos
                indevidos, presentes ou outras compensações indevidas em troca da compra e venda de produtos e serviços
                em nome da Empresa.</p><br>
            <p>A Plenum poderá, respeitados os princípios e valores deste Código e conforme políticas e procedimentos que
                venham a ser instituídos, oferecer aos clientes treinamentos, cursos, fomentar pesquisas e artigos científicos,
                visitas técnicas à Fábrica e custear algumas despesas disso decorrentes, desde que tais ofertas estejam em
                consonância com a condução normal e ética de seus negócios e também fortalecer a confiabilidade de
                produtos e serviços.
            </p><br>
            <h4>Produtos</h4>
            <p>Todos os produtos Plenum são fabricados sob um rígido controle de qualidade, utilizando apenas matériaprima
            com certificado de garantia e titânio comercialmente puro. Atendem às mais rigorosas normas técnicas
            e de segurança, possuindo Registro no Ministério da Saúde, fornecido pela
            Agência Nacional de Vigilância Sanitária (ANVISA), registro nos órgãos internacionais como: FDA e CE.</p><br>
            
            <h4>Serviços</h4>
            <p>Para total satisfação de nossos clientes, a Plenum oferece serviços de venda, pós-venda, consultoria técnica e
            assessoria comercial com profissionais altamente capacitados e permanentemente treinados.</p><br>
            <p>Nossos colaboradores estão aptos a esclarecer qualquer dúvida por meio de um moderno Serviço de
            Atendimento ao Consumidor no sistema 0800.</p><br>

            <h4>Fornecedores e Prestadores de Serviços</h4><br>
            <p>Os relacionamentos entre a Plenum e seus fornecedores de bens e serviços são pautados pela honestidade,
            transparência, tratamento igualitário em todos os atos, operações, negócios e transações</p><br>
            <p>Não é permitido o favorecimento de um determinado fornecedor por relações de interesses pessoais, sejam
            elas no âmbito familiar ou em conflito de interesse da Plenum e de suas políticas internas de compras, devendo
            sempre buscar o fornecedor que cumpra melhor os interesses da empresa.</p><br>
            <p>A Plenum busca aperfeiçoar os processos de aquisições que melhor atenda as suas necessidades e poderá
            desvincular em qualquer tempo suas relações com fornecedores que não atendam as exigências e
            qualificações necessárias para o bom andamento de seus processos ou quando estes não atribuirem uma
            caracterização clara de seus produtos ou serviços.</p><br>
            <p>Ainda quanto à desvinculação, a Plenum poderá dar por encerrada uma relação com seu fornecedor, quando
            este deixar de cumprir as cláusulas estipuladas em contrato.</p><br>
            <p>Em virtude da gestão de qualidade praticada pela Plenum, não poderão ser aceitos bens ou serviços de
            fornecedores que não possuam serviços de qualidade ou bens que não contenham especificações
            compatíveis com os princípios exigidos pela empresa.</p><br>
            <p>É terminantemente proibido a todos os colaboradores da Plenum, solicitar e/ou receber doações, prêmios,
            brindes ou patrocínios de qualquer fornecedor que atenda a empresa. Havendo qualquer insistência por parte
            dos fornecedores, tais brindes deverão ser encaminhados para a diretoria da empresa que, posteriormente
            doará a instituições de caridade.</p><br>
            <h4>Sindicatos</h4>
            <p>A Plenum reconhece os sindicatos da categoria dos colaboradores, bem como os sindicatos patronais da
            Indústria da qual a Plenum faz parte, como legítimos representantes das partes envolvidas nas relações
            trabalhistas e sindicais. A Plenum procura manter com tais entidades permanente diálogo, buscando a
            construção de relações de trabalho produtivas e harmoniosas.</p><br>

            <h4>Política de Qualidade</h4><br>
            <p>Todos os funcionários devem entender que, para manter a alta qualidade, é da maior importância que a
            Plenum atenda as expectativas dos clientes e usuários, na fabricação e comercialização de produtos
            odontológicos inovadores, de qualidade assegurada e dentro de um processo de melhoria contínua.</p><br>
            <h4>Conduta</h4><br>
            <p>Para a Plenum, os colaboradores são a base da realização de toda a estrutura, sendo sua participação
            fundamental nos processos de desenvolvimento da empresa. Os colaboradores são capazes de
            impulsionar mudanças e realizações hábeis para contribuir com o melhoramento contínuo dos processos.</p><br>
            <p><br>
            <p>
            Todos os colaboradores deverão estar preparados para conduzir os processos internos e externos, agir com
            respeito, ética, honestidade, compromisso, integridade e clareza em todas as relações de interesse da
            empresa e de seus clientes. Os colaboradores devem apresentar padrões de conduta inteiramente
            profissionais e que representam os valores e crenças pelos quais são conduzidos os negócios da Plenum.
            </p><br>
            <h4>Práticas Éticas de Negócios</h4><br>
            <p>Práticas comerciais justas e honestas são essenciais para o sucesso da Plenum. A empresa proíbe práticas de
            negócios antiéticas ou ilegais.</p><br>
            <p>Os colaboradores devem lidar de forma justa e honesta com parceiros de negócios da Plenum, clientes e
            concorrentes. Os colaboradores não devem tirar vantagem injusta pela manipulação, corrupção, dissimulação,
            abuso de informações privilegiadas, confidenciais ou informações de segredo comercial e industrial, deturpar
            fatos reais, ou qualquer prática desleal de negociação.</p><br>
            <h4>Concorrência Leal</h4><br>
            <p>As leis antitruste são projetadas para garantir uma concorrência livre e aberta. A este respeito, os
            colaboradores estão proibidos de colaborar com os concorrentes ou de tomar qualquer ação que poderia ter
            um efeito anti-competitivo, incluindo, mas não se limitando às seguintes situações:</p><br>
            <p>a) entabular acordos, entendimentos ou alianças que limitam ou se destinam a limitar a concorrência;<br>
            b) formalizar acordos ilegais ou adotar o uso de esquemas de preços artificiais para sufocar a concorrência.</p><br>
            <p>Para evitar até mesmo a ocorrência de uma violação antitruste, o colaborador nunca poderá se envolver em
            discussões com concorrentes sobre preços ou termos e condições em que os produtos são vendidos.<p><br>
            <h4>Práticas de Regulação e de Marketing</h4><br>
            <p>Os produtos comercializados pela Plenum são regulados por agências governamentais, ministérios da saúde e
            outros órgãos reguladores ou autoridades em todo o mundo. Exigências reguladoras também afetam a
            maneira pela qual a Plenum é capaz de comercializar seus produtos.</p> <br>
            <p>Cada colaborador é responsável pelo cumprimento dessas exigências e regras de comercialização.</p>
            <p>As práticas de publicidade e marketing da Plenum devem ser projetadas para transmitir informações honestas
            e fundamentadas sobre nossos produtos. A Plenum proíbe práticas de marketing ou vendas que são ilícitas,
            desonestas ou que violem as exigências regulatórias.</p>
            <h4>Exatidão de Registros Contábeis</h4><br>
            <p>Todos os ativos, passivos, receitas, despesas, e transações comerciais devem ser devidamente registrados
            com exatidão nos livros e registros da Empresa, de acordo com a legislação pertinente, com princípios
            contábeis geralmente aceitos e com as políticas e procedimentos financeiros
            estabelecidos pela Plenum.</p><br>
            <p>Não devem ser estabelecidos nem mantidos, para quaisquer objetivos que sejam, fundos de caixa ou outros
            bens secretos ou não registrados.</p><br>
            <h4>Imprensa e Internet</h4><br>
           <p>As relações da Plenum com os veículos de comunicação são de confiança e respeito mútuo. No entanto, fica
            vedado a todos os colaboradores a vinculação ou exposição da marca ou da imagem da empresa perante a
            imprensa ou internet, inclusive mídias sociais como o Facebook, Twitter, Instagram, dentre outros, sem prévia
            autorização da Alta Administração, a qual é responsável por qualquer menção da empresa nestes veículos.</p><br>
            <p>A publicidade e propaganda serão executadas com o objetivo de divulgação da empresa no mercado onde
            atua para promover seus produtos e ainda conscientizar a comunidade quanto aos benefícios da boa saúde.
            Cabe à Plenum respeitar todas as observações dadas pela legislação sobre os códigos profissionais e
            preceitos éticos no campo da imprensa em geral. Nenhum colaborador poderá utilizar informações da
            empresa em palestra, congressos, publicações anuais ou periódicos, seja no âmbito
            intelectual ou científico, sem a expressa autorização da Alta Administração.</p>
            <h4>Conflito de Interesses</h4>
            <p>O colaborador tem o dever de tomar decisões nos negócios visando proteger os melhores interesses da
            Plenum, sendo responsável por evitar qualquer situação que possa levar a um conflito de interesses ou
            dar início a um conflito.</p><br>
            <p>Conflitos de interesse podem surgir de várias maneiras, por exemplo: Quando os interesses particulares de um
            colaborador interferir ou puder interferir de alguma forma nos interesses da Plenum; Quando os
            colaboradores são orientados ou influenciados pelo ganho para si próprio, de seus familiares, ou por amizades
            pessoais; Quando um colaborador tem um interesse financeiro significativo em um negócio da Plenum ou com
            um concorrente da Plenum, ou Quando agrados extraordinários, como presentes, doações, diárias são aceitos
            de qualquer organização ou pessoa vinculada à empresa parceira de negócios com a Plenum ou concorrente
            da Plenum.</p><br>
            <p>Este Código não proíbe a aceitação de um presente ocasional de valor pequeno ou insignificante (ex.: brindes,
            canetas, calendários, agendas personalizadas, blocos de anotação personalizados, etc), desde que o
            recebimento do presente não crie um conflito de interesses. Sob nenhuma circunstância o colaborador poderá
            aceitar qualquer pagamento em dinheiro.</p><br>
            <p>Não são aceitas, dentro da política de gestão da Plenum, qualquer ação que desvalorize a imagem da empresa
            ou a integridade das informações que são consideradas confidenciais perante o mercado.</p><br>
            <p>Nenhum colaborador está habilitado a manipular informações confidenciais sem a expressa autorização da
            Diretoria da Plenum.</p><br>
            <p>Não é permitido aos colaboradores utilizarem o nome ou a marca Plenum sem o prévio consentimento da
            diretoria, especialmente para promover qualquer ato ligado a fins que não estão alinhados ao interesse da
            empresa, sendo terminantemente proibida a vinculação em propaganda ou publicidade ou, ainda, em ações
            ilícitas.</p><br>
            <p>A participação dos colaboradores da empresa em atividades ilícitas ou que venham a concorrer com a Plenum
            implicará na sua imediata desvinculação da empresa.</p><br>
            <h4>Oportunidades Iguais e Assédio</h4><br>
            <p>A Plenum está firmemente empenhada em proporcionar oportunidades iguais em todos os aspectos de
            emprego. Todo e qualquer tipo de discriminação, quer de natureza econômica, social, política, religiosa,
            quer de origem étnica, de sexo ou de idade, não é tolerado e constitui fato motivador de sanções. O mesmo
            ocorrerá em relação ao assédio moral, assédio sexual, tratamento desrespeitoso, descortês ou indigno a
            qualquer pessoa, independente de nível hierárquico, cargo ou função.</p><br>
            <h4>A Confidencialidade, Propriedade Intelectual e Informações Privilegiadas</h4><br>
            <p>A proteção das propriedades Plenum e a proteção das informações confidenciais são fundamentais para o
            sucesso da empresa.</p><br>
            <p>As informações confidenciais incluem todas as de natureza não pública sobre a Plenum e que tratem de
            propriedade intelectual, tais como patentes, marcas, desenhos, direitos autorais e know-how. As informações
            confidenciais também incluem segredos comerciais, tais como negócios e planos de marketing, ideias de
            engenharia e manufatura, bancos de dados, registros, informações salariais e pessoal, dados financeiros não
            publicados, relatórios internos, etc.</p><br>
            <p>O colaborador deverá proteger todas as informações confidenciais. A Plenum proíbe a divulgação de
            informações confidenciais a pessoas não autorizadas (incluindo não só profissionais não vinculados a Plenum,
            mas também colaboradores da Plenum que não são especificamente autorizados a ter acesso a tais
            informações).</p><br>
            <p>A utilização ou distribuição não autorizada de informações confidenciais podem prejudicar a posição da
            Plenum no mercado ou constituir uma violação de outros acordos ou contratos do qual ela faz parte.</p><br>
            <p>A prematura comunicação de idéias confidenciais, know-how e invenções para terceiros podem destruir ou
            impedir a proteção jurídica da propriedade intelectual da Plenum. Os colaboradores que lidam com
            informações confidenciais deverão adotar o cuidado de não deixar tais documentos acessíveis na sua mesa de
            trabalho enquanto ausentes.</p><br>
            <p>O uso de informações confidenciais para o benefício (financeiro) pessoal ou o fornecimento de informações
            confidenciais para outras pessoas que possam utilizá-las para benefício financeiro ou para tomar uma decisão
            de investimento não é só estritamente proibido por este Código, mas também constitui crime.</p><br>
            <h4>Obtenção de Informações Comerciais e Concorrência</h4><br>
            <p>Os colaboradores devem estar informados sobre o ambiente competitivo em que a Plenum está inserida e
            podem buscar informações com relação ao mercado e empresas concorrentes. No entanto, nunca poderão
            usar meios ilegais ou desonestos para adquirir informações confidenciais de terceiros, pessoas, empresas ou
            concorrentes da Plenum. Os colaboradores devem rejeitar informações confidenciais oferecidas de terceiros.</p><br>
            <h4>Ambiente, Saúde e Segurança</h4><br>
            <p>A Plenum está empenhada em proteger a saúde, o ambiente e a segurança de seus colaboradores.</p><br>
            <p>Espera-se que todos os colaboradores observem e cumpram as leis e procedimentos internos destinados a
            proteger a saúde e proteção no ambiente de trabalho. Os gestores devem incentivar os colaboradores a
            observarem a preservação ambiental, segurança e saúde como parte integrante de suas responsabilidades
            diárias.</p><br>
            <h4>Fiscalização</h4><br>
            <p>Em caso de uma fiscalização de qualquer órgão público, regulatório, autarquias, etc., o colaborador que
            receber a fiscalização deverá notificar imediatamente o departamento jurídico para que os procedimentos
            internos possam ser feitos visando o pleno cumprimento das obrigações legais.</p><br>
            <p>Durante qualquer fiscalização, não é permitido destruir ou alterar quaisquer documentos da Plenum, omitir, ou
            fazer declarações inverídicas.</p><br>
            <h4>Tratamento das Violações</h4><br>
            <p>O colaborador que tiver conhecimento de qualquer atividade de outro colaborador que possa ser
            incompatível com qualquer disposição deste Código de Conduta, Políticas, Procedimentos ou qualquer lei ou
            regulamento aplicável à empresa, deverá informar o Departamento Jurídico.</p><br>
            <p>As investigações serão realizadas, sempre que possível, em sigilo e de acordo com os procedimentos internos
            e leis aplicáveis ao caso.</p><br>
            <p>A Plenum não retaliará ou adotará qualquer conduta coercitiva contra o colaborador que denunciar ou auxiliar
            na investigação.</p><br>
            <h4>As Medidas Disciplinares</h4><br>
            <p>Os colaboradores devem adotar comportamento adequado, de acordo com este Código de Conduta.</p><br>
            <p>A não observância deste código poderá acarretar em sanções previstas no próprio Código, na legislação e até
            mesmo demissão.</p><br>
            <h4>Acompanhamento do Cumprimento do Código de Conduta</h4><br>
            <p>A Alta Administração da Plenum é responsável por supervisionar o cumprimento deste Código.</p><br>
            <p>Os supervisores e gerentes devem garantir que os colaboradores sob sua supervisão compreendam e
            cumpram este Código. Os consultores da Plenum também estão vinculados a este Código.</p><br>
            <p>Se você tiver alguma dúvida ou sugestão em relação a este Código, por favor, consulte o departamento
            jurídico da Plenum.</p><br>
            <p>As diretrizes aqui apresentadas tornam-se normas regulamentadoras internas da empresa Plenum, cuja
            violação fica sujeita a sanções disciplinares, observadas a natureza e o grau da infração, podendo ainda
            acarretar em sanções de ordem civil ou criminal que terão tratamento legal pertinente.</p><br>
            <p>A Plenum desde já assume o compromisso com sigilo, seriedade e cautela quanto a possíveis denúncias de
            desrespeito as diretrizes éticas apresentada neste documento.</p><br>

