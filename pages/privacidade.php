            <h3>POLÍTICA DE PRIVACIDADE DE DADOS DE CLIENTES PLENUM</h3><br>
           <h4>1. OBJETIVO</h4><br>

            <p>A Plenum está comprometida com a confidencialidade e privacidade das informações de seus clientes. Por
            isto adota práticas que proporcionam segurança e transparência no registro e uso autorizado de dados. O
            presente documento tem como objetivo apresentar as principais diretrizes dessa política.</p><br>
            <h4>2. DEFINIÇÕES</h4>
            <p>Cliente: Toda e qualquer pessoa, física ou jurídica, que tem interesse, adquire ou adquiriu produtos e serviços
            da Plenum.</p><br>
            <h4>3. CAMPO DE APLICAÇÃO</h4>
            <p>Esta política aplica-se a toda e qualquer ação ou pessoa (física ou jurídica) que tenha acesso a informações de
            clientes da Plenum.</p><br>
            <h4>4. PRIVACIDADE E CONFIDENCIALIDADE</h4>
            <p>A Plenum possui o compromisso de zelar e tratar corretamente todas as informações recebidas de seus
            clientes, especialmente dados pessoais e informações financeiras, tratando-as como informações
            confidenciais.</p><br>
            <p>Todas as informações pessoais relativas à clientes serão consideradas informações confidenciais, exceto com
            autorização expressa para uso público.</p><br>
            <p>As informações de clientes não poderão ser utilizadas ou repassadas, para benefício próprio ou de terceiros,
            quer seja de forma gratuita ou onerosa, sem o prévio consentimento expresso do cliente.
            É vedado aos colaboradores da Plenum induzir ou tentar induzir outros colaboradores à prática de atos ilícitos,
            decorrentes da manipulação das informações confidenciais de clientes.</p><br>
            <h4>5. QUAIS INFORMAÇÕES COLETADA SEM CADASTRO E ATENDIMENTO</h4>
            <p>São objeto de registro em nossos sistemas de informação os dados abaixo:</p>
              
              <li>Dados comerciais (Nome da Pessoa Jurídica, Endereço, CNPJ);</li>
              <li>Dados pessoais (Nome, CPF, endereço, telefone, e-mail, registro profissional, data de nascimento e gênero);</li>
              <li>Dados financeiros (bancários);</li>
              <li>Registros de itens comprados, quantidades e valores (desde 05/2019);</li>
              <li>Registros de atendimentos realizados pelo SAC - Serviço de Atendimento ao Consumidor (desde 05/2019);</li>
              <li>Registros de atendimento realizados pela Ouvidoria (desde 05/2019);</li>
              <li>E-mails enviados e recebidos.</li>
            <br>
            <p>Eventualmente, poderemos utilizar ferramentas tecnológicas conhecidas e autorizadas (cookies, java, etc.)
            para confirmar identidade, personalizar acesso e acompanhar a utilização, sempre em busca de
            aprimoramento da melhor navegação e funcionalidades ao cliente.</p><br>
            <h4>6. USO DE INFORMAÇÕES COLETADAS PELA PLENUM</h4><br>
            <p>Em conformidade com as disposições vigentes nos países em que a Plenum atua, as informações referentes
            aos dados fornecidos por nossos clientes via telefone, aplicativo ou site serão de uso exclusivo da Plenum,
            devendo ser utilizadas única e exclusivamente para as finalidades para as quais as mesmas foram cedidas por
            nossos clientes.</p><br>
            <p>A Plenum utilizará as informações fornecidas por seus clientes para a finalidade de confirmação de dados e
            concretização das negociações comerciais firmadas com estes clientes, manutenção de rastreabilidade
            regulatória e ainda para propósitos fisco-legais, podendo ainda utilizar as mesmas para eventuais ações
            internas que tenham ligação com o cliente, inclusive ações de marketing e promoção de seus produtos.</p><br>
            <p>As informações financeiras coletadas destinam-se apenas a aprovação de crédito e aquisição de produtos e
            serviços.</p>
            <p>As informações coletadas via telefone, aplicativo ou site são tratadas somente por pessoas qualificadas e
            autorizadas para tanto, tendo o dever de manter a confidencialidade e sigilo de todos os dados.</p><br>
            <p>O colaborador Plenum que manipular indevidamente e/ou de forma não autorizada informações consideradas
            confidenciais, comete falta grave e estará sujeito a ação disciplinar, com aplicação das sanções cabíveis.</p><br>
            <p>Nenhuma informação fornecida por nossos clientes será fornecida a terceiros, de forma gratuita ou onerosa,
            salvo com autorização expressa do cliente ou em caso de determinação judicial ou por força de lei.</p><br>
            <h4>7. TRANSFERÊNCIA DE INFORMAÇÕES COLETADAS PELA PLENUM</h4><br>
            <p>A Plenum poderá transferir, internamente, as informações coletadas entre empresas coligadas e
            distribuidores, para fins de unificação de cadastro. No caso de transferência de informações para empresas
            Plenum localizadas em países estrangeiros, a Plenum compromete-se a manter a confidencialidade e
            privacidade das mesmas, de acordo com o previsto nesta política e à legislação de cada país.</p><br>
            <h4>8. ACESSO DO CLIENTE AS INFORMAÇÕES</h4><br>
            <p>O cliente tem o direito de solicitar as informações existentes em seu nome nos bancos de dados da Plenum,
            através de solicitação formal pelo site da Plenum, em Fale Conosco, no endereço<a class="link_privacidade" href="#">www.plenum.bio/contato/</a>,
            por e-mail em sac@plenum.bio ou 0800-000000, sendo necessária a confirmação de alguns dados e
            documentos para disponibilização do relatório.</p><br>
            <br>O cliente pode solicitar, a qualquer tempo, a atualização ou correção dos seus dados, por telefone ou pelo site,
            também em Fale Conosco, no endereço <a a class="link_privacidade" href="#">www.plenum.bio/contato/</a>, por e-mail em <a a class="link_privacidade" href="#">sac@plenum.bio</a> ou 0800-
            000000.</p><br>
            <p>O cliente pode relatar à Plenum qualquer uso indevido de informações fornecidas ou realizar denúncias sobre
            qualquer situação que entender ser má conduta no uso da informação de clientes, através do site da Plenum,
            em Canal de Denúncia (a denúncia pode ser anônima), por e-mail em<a a class="link_privacidade" href="#">compliance@plenum.bio.</a>  </p><br>
            <h4>9. ALTERAÇÃO NA POLÍTICA DE PRIVACIDADE DE DADOS DE CLIENTES</h4><br>
            <p>A Plenum poderá, a qualquer tempo, alterar as disposições constantes nesta política, sendo que quaisquer
            alterações serão publicadas no site da Empresa.</p><br>
            <p>Caso o cliente possua alguma dúvida sobre o uso dos dados de clientes pela Plenum, não descrito nesta
            política, poderá saná-la através dos e-mails <a a class="link_privacidade" href="#">sac@plenum.bio</a> ou <a a class="link_privacidade" href="#">compliance@plenum.bio</a></p>
            <h4>10. DISPOSIÇÕES GERAIS</h4><br>
            <p>Os colaboradores da Plenum comprometem-se em compreender e cumprir integralmente as disposições
            descritas nesta política.</p><br>
            <p>As violações a esta política estão sujeitas a aplicação de sanções previstas em normas internas, medidas
            disciplinares e demais sanções previstas na legislação brasileira vigente, e dos países onde a Plenum realiza
            negócios.</p><br> 
            <p>O vazamento de informação confidencial em desrespeito a esta política, ao Código de Conduta Plenum
            (www.plenum.bio/código-de-conduta/) e demais legislações e regulamentos, também poderá implicar na
            responsabilização civil, administrativa e criminal do responsável, incluindo o oferecimento de representação
            ou queixa-crime, bem como ações por perdas e danos.<p><br>
            <p>A presente política tem cumprimento obrigatório após sua aprovação pela Diretoria da Plenum, bem como
            sua publicação na intranet e no site da Plenum.<p><br>
            <p>Esta política foi elaborada em consonância com a legislação vigente no Brasil, com especial atenção as
            orientações da lei 12.965/2014 e 8.078/1990.<p>
